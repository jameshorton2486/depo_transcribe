def apply_case_corrections(
    text: str,
    job_config: JobConfig,
    records: List[CorrectionRecord],
    block_index: int,
    state: CorrectionState | None = None,
) -> str:
    confirmed_spellings = _resolve_confirmed_spellings(job_config)

    for wrong, correct in confirmed_spellings.items():
        pattern = r'\b' + re.escape(wrong) + r'\b'

        def _replace_with_case(m: re.Match, _correct: str = correct) -> str:
            return _preserve_case(m.group(0), _correct)

        new_text = re.sub(pattern, _replace_with_case, text, flags=re.IGNORECASE)
        text = _apply_safe_rewrite(
            text,
            new_text,
            records,
            block_index,
            f"confirmed_spelling:{wrong}",
            record_original=wrong,
            record_corrected=correct,
            state=state,
        )
    return text


