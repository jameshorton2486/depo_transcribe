# SPOKEN DIGIT / NUMBER COLLAPSE HELPERS
# ═══════════════════════════════════════════════════════════════════════════

def _collapse_spoken_digits(token_sequence: str) -> str:
    """
    Convert spoken digit words and letter tokens into a compact token string.

    This is a generic helper for cause numbers, CSR numbers, addresses, and
    similar Texas deposition numeric identifiers when smart_format is disabled.
    """
    tokens = token_sequence.strip().lower().split()
    result: list[str] = []
    i = 0
    while i < len(tokens):
        token = tokens[i]

        if i + 1 < len(tokens):
            two = token + " " + tokens[i + 1]
            if two in _LETTER_GROUPS:
                result.append(_LETTER_GROUPS[two])
                i += 2
                continue

        if token in _WORD_TO_DIGIT:
            result.append(_WORD_TO_DIGIT[token])
            i += 1
            continue

        if re.match(r'^[a-z]$', token):
            result.append(token.upper())
            i += 1
            continue

        if token.upper() in _LETTER_GROUPS.values():
            result.append(token.upper())
            i += 1
            continue

        if result and not result[-1].endswith(" "):
            result.append(" ")
        result.append(token)
        if i + 1 < len(tokens):
            result.append(" ")
        i += 1

    return "".join(result).strip()


def _spoken_year_to_int(year_str: str) -> str | None:
    """
    Convert a spoken year phrase into a four-digit year string when unambiguous.
    """
    y = re.sub(r'\s+', ' ', year_str.lower().replace('-', ' ')).strip()

    if y == 'twenty twenty':
        return '2020'

    m = re.match(
        r'^twenty (eighteen|nineteen)$',
        y,
    )
    if m:
        suffix = {'eighteen': '18', 'nineteen': '19'}[m.group(1)]
        return f"20{suffix}"

    m = re.match(r'^twenty (one|two|three|four|five|six|seven|eight|nine)$', y)
    if m:
        suffix = {
            'one': '21', 'two': '22', 'three': '23', 'four': '24',
            'five': '25', 'six': '26', 'seven': '27', 'eight': '28', 'nine': '29',
        }[m.group(1)]
        return f"20{suffix}"

    m = re.match(
        r'^twenty twenty (one|two|three|four|five|six|seven|eight|nine)$',
        y,
    )
    if m:
        suffix = {
            'one': '1', 'two': '2', 'three': '3', 'four': '4',
            'five': '5', 'six': '6', 'seven': '7', 'eight': '8', 'nine': '9',
        }[m.group(1)]
        return f"202{suffix}"

    if y in {'twenty twenty four', 'twenty twenty-five', 'twenty twenty five'}:
        return '2025' if 'five' in y else '2024'
    if y in {'twenty twenty six', 'twenty twenty-six'}:
        return '2026'

    m = re.match(
        r'^nineteen (eighty|ninety)(?: (one|two|three|four|five|six|seven|eight|nine|ten))?$',
        y,
    )
    if m:
        decade = {'eighty': '8', 'ninety': '9'}[m.group(1)]
        ones = {
            None: '0', 'one': '1', 'two': '2', 'three': '3', 'four': '4',
            'five': '5', 'six': '6', 'seven': '7', 'eight': '8', 'nine': '9',
            'ten': '0',
        }[m.group(2)]
        return f"19{decade}{ones}"

    m = re.match(
        r'^two thousand(?: and)?(?: (one|two|three|four|five|six|seven|eight|nine|'
        r'ten|eleven|twelve|thirteen|fourteen|fifteen|sixteen|seventeen|'
        r'eighteen|nineteen|twenty))?$',
        y,
    )
    if m:
        suffix = {
            None: '00', 'one': '01', 'two': '02', 'three': '03', 'four': '04',
            'five': '05', 'six': '06', 'seven': '07', 'eight': '08', 'nine': '09',
            'ten': '10', 'eleven': '11', 'twelve': '12', 'thirteen': '13',
            'fourteen': '14', 'fifteen': '15', 'sixteen': '16', 'seventeen': '17',
            'eighteen': '18', 'nineteen': '19', 'twenty': '20',
        }[m.group(1)]
        return f"20{suffix}"

    return None


def _small_number_phrase_to_int(phrase: str) -> int | None:
    tokens = [t for t in re.sub(r"[-,]", " ", phrase.lower()).split() if t and t != "and"]
    if not tokens:
        return None

    total = 0
    current = 0
    for token in tokens:
        if token == "hundred":
            if current == 0:
                current = 1
            current *= 100
            continue
        if token == "thousand":
            if current == 0:
                current = 1
            total += current * 1000
            current = 0
            continue
        value = _SMALL_NUMBER_WORDS.get(token)
        if value is None:
            return None
        current += value
    return total + current


def _collapse_small_number_chunks(phrase: str) -> str | None:
    tokens = [t for t in re.sub(r"[-,]", " ", phrase.lower()).split() if t]
    if not tokens:
        return None

    chunks: list[str] = []
    i = 0
    while i < len(tokens):
        token = tokens[i]
        if token in {
            "twenty", "thirty", "forty", "fifty", "sixty", "seventy",
            "eighty", "ninety",
        } and i + 1 < len(tokens) and tokens[i + 1] in {
            "one", "two", "three", "four", "five", "six", "seven", "eight", "nine",
        }:
            chunks.append(str((_SMALL_NUMBER_WORDS[token]) + _SMALL_NUMBER_WORDS[tokens[i + 1]]))
            i += 2
            continue

        value = _SMALL_NUMBER_WORDS.get(token)
        if value is None:
            return None
        chunks.append(str(value))
        i += 1

    return "".join(chunks) if chunks else None


