# SAFE-APPLY CORE
# ═══════════════════════════════════════════════════════════════════════════

def safe_apply(
    text: str,
    new_text: str,
    pattern: str,
    state: CorrectionState | None,
    records: List[CorrectionRecord],
    block_index: int,
    *,
    record_original: str | None = None,
    record_corrected: str | None = None,
    allow_shortening: bool = False,
    protected_after: str | None = None,
) -> str:
    """
    Apply a correction only when it is non-destructive and non-conflicting.

    Both rejection paths (too-short result, conflict with earlier rule) emit a
    WARNING-level log line. We previously also printed to stdout — that's been
    removed because it bypassed log handlers and caused noise in production.
    """
    if new_text == text:
        return text
    if not _is_safe_rewrite(text, new_text) and not allow_shortening:
        logger.warning(
            "[corrections] SKIPPED (result too short) pattern=%s block=%d "
            "before_len=%d after_len=%d",
            pattern, block_index, len(text), len(new_text),
        )
        return text
    if state is not None and state.would_conflict(new_text):
        logger.warning(
            "[corrections] SKIPPED (conflict with earlier rule) pattern=%s block=%d",
            pattern, block_index,
        )
        return text
    if state is not None:
        state.record(pattern, text, new_text, protected_after=protected_after)
    records.append(CorrectionRecord(
        original=record_original if record_original is not None else text,
        corrected=record_corrected if record_corrected is not None else new_text,
        pattern=pattern,
        block_index=block_index,
    ))
    return new_text


def _apply_safe_rewrite(
    original_text: str,
    new_text: str,
    records: List[CorrectionRecord],
    block_index: int,
    pattern: str,
    record_original: str | None = None,
    record_corrected: str | None = None,
    state: CorrectionState | None = None,
    protected_after: str | None = None,
) -> str:
    compact_date_output = bool(
        re.search(r'\b\d{2}/\d{2}/\d{4}\b', new_text)
        or re.search(
            r"\b(?:January|February|March|April|May|June|July|August|"
            r"September|October|November|December)\s+of\s+'?\d{2}\b",
            new_text,
            flags=re.IGNORECASE,
        )
    )
    allow_shortening = (
        pattern == 'artifact_duplicate_4plus'
        and bool(ARTIFACT_DUPLICATE_RE.search(original_text))
    ) or (
        pattern == 'fix_spoken_dates'
        and compact_date_output
    ) or (
        # Spoken-digit collapse for cause numbers is inherently a >50%
        # reduction (eleven words like "two zero two five C I two three
        # two six seven" → "2025CI23267"). MIN_REWRITE_RATIO=0.5
        # otherwise silently discards it. The guard requires a real
        # "Cause Number" / "Cause No." prefix in the ORIGINAL text
        # before authorizing the shortening, so this can't be abused
        # if the rule's regex is ever weakened.
        pattern == 'fix_cause_number_digits'
        and bool(re.search(
            r'\b(Cause\s+No\.?|Cause\s+Number)\b',
            original_text,
            re.IGNORECASE,
        ))
    ) or (
        # Spoken-time conversion ("Eight forty five PM." → "8:45 p.m.")
        # is also inherently a >50% reduction. Same defensive prefix
        # pattern as fix_cause_number_digits — only authorize shortening
        # when the original text actually contains an a.m./p.m. shape
        # the rule could legitimately have processed.
        pattern == 'fix_spoken_times'
        and bool(re.search(
            r'\b(?:[Aa]\.?[Mm]\.?|[Pp]\.?[Mm]\.?)\b',
            original_text,
        ))
    )
    return safe_apply(
        original_text,
        new_text,
        pattern,
        state,
        records,
        block_index,
        record_original=record_original,
        record_corrected=record_corrected,
        allow_shortening=allow_shortening,
        protected_after=protected_after,
    )


def _block_start_seconds(block: Block) -> float | None:
    start = block.meta.get("start") if isinstance(block.meta, dict) else None
    if isinstance(start, (int, float)):
        return float(start)
    if block.words:
        first_start = getattr(block.words[0], "start", None)
        if isinstance(first_start, (int, float)):
            return float(first_start)
    return None


