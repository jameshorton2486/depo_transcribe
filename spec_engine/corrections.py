def fix_traffic_citation_mishearing(text: str) -> str:
    """
    Replace "sanitation" with "citation" only in traffic/legal contexts.

    This stays separate from the universal legal phrase list because it is
    context-dependent and should not fire in ordinary sanitation contexts.
    """
    traffic_context = re.compile(
        r'\b(ticket|officer|pulled\s+over|traffic|violation|speeding|'
        r'DPS|fine|court|charge|police|conviction|cited|license|citation)\b',
        re.IGNORECASE,
    )

    def _replace(segment: str) -> str:
        def _replace_match(match: re.Match) -> str:
            prefix = segment[:match.start()]
            suffix = segment[match.end():]
            prefix_tokens = re.findall(r'\b[\w-]+\b', prefix)[-6:]
            suffix_tokens = re.findall(r'\b[\w-]+\b', suffix)[:6]
            local_window = ' '.join(prefix_tokens + [match.group(0)] + suffix_tokens)
            if traffic_context.search(local_window):
                return _preserve_phrase_case(match.group(0), 'citation')
            return match.group(0)

        return re.sub(
            r'\bsanitation\b',
            _replace_match,
            segment,
            flags=re.IGNORECASE,
        )

    return _apply_outside_protected_segments(text, _replace)


def apply_texas_deposition_seeds(text: str, confirmed_spellings: dict) -> str:
    """
    Apply static Texas deposition vocabulary corrections as a fallback layer.

    Case-specific confirmed_spellings always win. Seeds only fire when the
    same source phrase is not already handled by the case-specific map.
    """
    handled = {str(key).lower() for key in (confirmed_spellings or {}).keys()}

    def _replace(segment: str) -> str:
        working = segment
        for wrong, correct in TEXAS_DEPOSITION_SEEDS.items():
            if wrong.lower() in handled:
                continue
            pattern = r'\b' + re.escape(wrong) + r'\b'
            working = re.sub(
                pattern,
                lambda m, _correct=correct: _preserve_phrase_case(m.group(0), _correct),
                working,
                flags=re.IGNORECASE,
            )
        return working

    return _apply_outside_protected_segments(text, _replace)


